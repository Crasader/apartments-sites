  <div id="sec-title">Before You Sign a Lease</div>
  <div style="text-align:right; margin:0 10px 0 0;"><a href="javascript: history.go(-1)">< Back</a></div>
  A lease is a binding contract, so be sure you understand your rights and obligations before you sign.<br />
  <br />
  <span class="bold">Questions to Ask a Property Manager</span><br />
  <ul>
  	<li>What length lease is available or required? (monthly, 6 months, 1 year, 2 years)</li>
		<li>Exactly when is the apartment available for you to move in?</li>
		<li>Are utilities included in your rent?</li>
		<li>How much security is required?</li>
		<li>Will you be subject to a credit check?</li>
		<li>When is the rent due?</li>
		<li>What form of payment is required?</li>
		<li>Is there a penalty for late payment?</li>
		<li>Are there penalties if you terminate before lease expires?</li>
		<li>Are there restrictions on pets?</li>
		<li>Will it be freshly painted before you move in?</li>
		<li>Is extra storage space available for bikes and large objects?</li>
		<li>Are there special hours for moving in?</li>
		<li>Are you required to use special elevators or stairs?</li>
		<li>Are there laundry facilities on the premises?</li>
		<li>Is free parking available?</li>
		<li>Are there limits on the number of vehicles?</li>
	</ul>
